// O método reverse() altera o array original e cria um novo array com a posição dos elementos invertidos.
const array23: number[] = [6, 5, 4, 3 , 2, 1];
const reversedArray23: number[] = array23.reverse();
console.log(reversedArray23); // [1, 2, 3, 4, 5, 6]
console.log(array23); // [1, 2, 3, 4, 5, 6]
